function out = run_stage05_formal_suite(varargin)
%RUN_STAGE05_FORMAL_SUITE Run the formal Stage05 experiment suite.
%
% Current scope:
%   1) legacy reproduction formal
%   2) OpenD manual-RAAN formal
%   3) ClosedD manual-RAAN formal

suite = make_stage05_formal_suite_spec(varargin{:});

out = struct();
out.suite_spec = suite;
out.started_at = string(datetime('now'));

common_args = { ...
    'profile', suite.profile, ...
    'i_grid_deg', suite.i_grid_deg, ...
    'P_grid', suite.P_grid, ...
    'T_grid', suite.T_grid, ...
    'h_fixed_km', suite.h_fixed_km, ...
    'F_fixed', suite.F_fixed, ...
    'plot_visible', suite.plot_visible, ...
    'save_cache', suite.save_cache, ...
    'use_parallel', suite.use_parallel, ...
    'show_progress', suite.show_progress};

out.legacy_reproduction = run_stage05_opend_legacy_reproduction_framework( ...
    common_args{:}, ...
    'artifact_root', suite.legacy_artifact_root, ...
    'output_suffix', 'formal');

out.opend_manual_raan = run_stage05_opend_manual_raan_fullgrid_experiment( ...
    common_args{:}, ...
    'raan_range_deg', suite.raan_range_deg, ...
    'raan_step_deg', suite.raan_step_deg, ...
    'artifact_root', suite.opend_artifact_root, ...
    'output_suffix', 'formal');

out.closedd_manual_raan = run_stage05_closedd_manual_raan_fullgrid_experiment( ...
    common_args{:}, ...
    'raan_range_deg', suite.raan_range_deg, ...
    'raan_step_deg', suite.raan_step_deg, ...
    'artifact_root', suite.closedd_artifact_root, ...
    'output_suffix', 'formal');

out.finished_at = string(datetime('now'));
out.manifest = write_stage05_formal_suite_manifest(out);
end
