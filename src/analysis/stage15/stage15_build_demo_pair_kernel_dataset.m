function dataset = stage15_build_demo_pair_kernel_dataset(box)
%STAGE15_BUILD_DEMO_PAIR_KERNEL_DATASET
% Minimal demo dataset for Stage15-B.
%
% target_state = [x_km, y_km, vx_mps, vy_mps]

dataset = struct( ...
    'sample_id', {}, ...
    'box', {}, ...
    'target_state', {}, ...
    'sat1_xy_km', {}, ...
    'sat2_xy_km', {}, ...
    'xi', {}, ...
    'kappa2', {}, ...
    'risk_label', {});

samples = { ...
    struct('id', 'S01', 'target', [200, 300, 4200, 1800], 'sat1', [-600, -200], 'sat2', [700, -150]), ...
    struct('id', 'S02', 'target', [400, 100, 4500,  800], 'sat1', [-700, -100], 'sat2', [650,  250]), ...
    struct('id', 'S03', 'target', [100, 500, 3600, 2500], 'sat1', [-500, -300], 'sat2', [200, -600]), ...
    struct('id', 'S04', 'target', [600, 200, 4200, 1000], 'sat1', [ 100, -800], 'sat2', [900, -700]), ...
    struct('id', 'S05', 'target', [300, 700, 2800, 3200], 'sat1', [-300, -700], 'sat2', [500,  100]), ...
    struct('id', 'S06', 'target', [800, 100, 5000,  300], 'sat1', [-200, -100], 'sat2', [950,   50]), ...
    struct('id', 'S07', 'target', [500, 500, 3500, 2200], 'sat1', [-700,  100], 'sat2', [200,  900]), ...
    struct('id', 'S08', 'target', [150, 150, 4700, 1200], 'sat1', [ 800, -600], 'sat2', [850,  600]) ...
    };

for i = 1:numel(samples)
    s = samples{i};
    rec = stage15_make_sample_record(s.id, box, s.target, s.sat1, s.sat2);
    dataset(end+1) = rec; %#ok<AGROW>
end
end
