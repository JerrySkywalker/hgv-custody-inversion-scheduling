function out = run_stage05_validation_suite(varargin)
%RUN_STAGE05_VALIDATION_SUITE Run strict validation suite for Stage05 legacy reproduction.

suite = make_stage05_validation_suite_spec(varargin{:});

% Reduce startup noise inside suite runs.
setenv('HGV_STARTUP_LOG_REPEATED_INIT', 'false');

out = struct();
out.suite_spec = suite;
out.started_at = string(datetime('now'));

common_args = { ...
    'profile', suite.profile, ...
    'i_grid_deg', suite.i_grid_deg, ...
    'P_grid', suite.P_grid, ...
    'T_grid', suite.T_grid, ...
    'h_fixed_km', suite.h_fixed_km, ...
    'F_fixed', suite.F_fixed, ...
    'plot_visible', suite.plot_visible, ...
    'artifact_root', suite.reproduction_artifact_root, ...
    'output_suffix', 'validation', ...
    'save_cache', suite.save_cache, ...
    'use_parallel', suite.use_parallel, ...
    'show_progress', suite.show_progress};

out.reproduction = run_stage05_opend_legacy_reproduction_framework(common_args{:});

out.compare = manual_smoke_stage05_opend_legacy_reproduction_compare( ...
    'framework_result', out.reproduction, ...
    'artifact_root', suite.validation_artifact_root);

assert_stage05_opend_legacy_reproduction_compare_result(out.compare);

out.finished_at = string(datetime('now'));
out.manifest = write_stage05_validation_suite_manifest(out);
end
