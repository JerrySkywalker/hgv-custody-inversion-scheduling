function result = stage15_eval_template_library_3d(dataset, template_library)
%STAGE15_EVAL_TEMPLATE_LIBRARY_3D
% Evaluate nearest-template matching on 3D staticworld dataset.

matches = struct( ...
    'sample_id', {}, ...
    'true_label', {}, ...
    'true_geometry_class', {}, ...
    'true_layout_class', {}, ...
    'matched_template_id', {}, ...
    'matched_label', {}, ...
    'matched_geometry_class', {}, ...
    'matched_layout_class', {}, ...
    'distance', {}, ...
    'is_correct', {});

for i = 1:numel(dataset)
    m = stage15_match_template_for_sample_3d(dataset(i), template_library);
    matches(end+1) = m; %#ok<AGROW>
end

num_correct = sum([matches.is_correct]);
num_samples = numel(matches);
accuracy = num_correct / max(num_samples, 1);

result = struct();
result.matches = matches;
result.num_samples = num_samples;
result.num_correct = num_correct;
result.accuracy = accuracy;
end
