function summary_tbl = build_stage05_formal_suite_summary_table(out)
%BUILD_STAGE05_FORMAL_SUITE_SUMMARY_TABLE Build summary table from Stage05 formal suite outputs.

rows = {};

% legacy reproduction
legacy_env = out.legacy_reproduction.outputs.best_pass_by_Ns;
legacy_truth = out.legacy_reproduction.outputs.truth_table;

legacy_min_feasible_Ns = NaN;
legacy_P = NaN;
legacy_T = NaN;
legacy_mean_pass_ratio = NaN;
legacy_max_pass_ratio = NaN;
legacy_min_pass_ratio = NaN;

if ~isempty(legacy_env)
    legacy_mean_pass_ratio = mean(legacy_env.pass_ratio, 'omitnan');
    legacy_max_pass_ratio = max(legacy_env.pass_ratio, [], 'omitnan');
    legacy_min_pass_ratio = min(legacy_env.pass_ratio, [], 'omitnan');

    feasible_mask = legacy_env.pass_ratio >= 1;
    idx = find(feasible_mask, 1, 'first');
    if ~isempty(idx)
        legacy_min_feasible_Ns = legacy_env.Ns(idx);

        if ~isempty(legacy_truth)
            match_idx = find(legacy_truth.Ns == legacy_min_feasible_Ns, 1, 'first');
            if ~isempty(match_idx)
                legacy_P = legacy_truth.P(match_idx);
                legacy_T = legacy_truth.T(match_idx);
            end
        end
    end
end

rows(end+1, :) = { ...
    "legacy_reproduction", ...
    legacy_P, ...
    legacy_T, ...
    legacy_min_feasible_Ns, ...
    NaN, ...
    NaN, ...
    NaN, ...
    legacy_min_pass_ratio, ...
    legacy_max_pass_ratio, ...
    legacy_mean_pass_ratio, ...
    NaN};

% OpenD manual-RAAN
opend_agg = out.opend_manual_raan.outputs.agg_by_base_design;
for i = 1:height(opend_agg)
    rows(end+1, :) = { ...
        "opend_manual_raan", ...
        opend_agg.P(i), ...
        opend_agg.T(i), ...
        opend_agg.Ns(i), ...
        opend_agg.min_DG_rob(i), ...
        opend_agg.max_DG_rob(i), ...
        opend_agg.mean_DG_rob(i), ...
        opend_agg.min_pass_ratio(i), ...
        opend_agg.max_pass_ratio(i), ...
        opend_agg.mean_pass_ratio(i), ...
        NaN};
end

% ClosedD manual-RAAN
closedd_agg = out.closedd_manual_raan.outputs.agg_by_base_design;
for i = 1:height(closedd_agg)
    rows(end+1, :) = { ...
        "closedd_manual_raan", ...
        closedd_agg.P(i), ...
        closedd_agg.T(i), ...
        closedd_agg.Ns(i), ...
        NaN, ...
        NaN, ...
        NaN, ...
        closedd_agg.min_pass_ratio(i), ...
        closedd_agg.max_pass_ratio(i), ...
        closedd_agg.mean_pass_ratio(i), ...
        closedd_agg.mean_joint_margin(i)};
end

summary_tbl = cell2table(rows, 'VariableNames', { ...
    'branch', ...
    'P', ...
    'T', ...
    'Ns', ...
    'min_DG_rob', ...
    'max_DG_rob', ...
    'mean_DG_rob', ...
    'min_pass_ratio', ...
    'max_pass_ratio', ...
    'mean_pass_ratio', ...
    'mean_joint_margin'});
end
