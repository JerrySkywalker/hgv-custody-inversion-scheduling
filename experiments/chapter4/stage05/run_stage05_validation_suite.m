    'i_grid_deg', suite.i_grid_deg, ...
    'P_grid', suite.P_grid, ...
    'T_grid', suite.T_grid, ...
    'h_fixed_km', suite.h_fixed_km, ...
    'F_fixed', suite.F_fixed, ...
    'plot_visible', suite.plot_visible, ...
    'artifact_root', suite.reproduction_artifact_root, ...
    'output_suffix', 'validation', ...
