function bank = stage15_build_staticworld_sensor_pair_bank_3d()
%STAGE15_BUILD_STATICWORLD_SENSOR_PAIR_BANK_3D
% Representative 3D sensor-pair layouts.
%
% sat_state = [x_km, y_km, z_km, vx_mps, vy_mps, vz_mps]

bank = struct( ...
    'pair_id', {}, ...
    'layout_class', {}, ...
    'sat1_state', {}, ...
    'sat2_state', {});

rows = { ...
    {'P001','wide_balanced'  ,[-800,-300,850,0,0,0],[ 800,-300,850,0,0,0]}, ...
    {'P002','wide_balanced'  ,[-900, 100,900,0,0,0],[ 900, 100,900,0,0,0]}, ...
    {'P003','narrow_balanced',[-300,-500,820,0,0,0],[ 300,-500,820,0,0,0]}, ...
    {'P004','narrow_balanced',[-250, 450,820,0,0,0],[ 250, 450,820,0,0,0]}, ...
    {'P005','asym_left'      ,[-850, 150,880,0,0,0],[ 200,-650,780,0,0,0]}, ...
    {'P006','asym_left'      ,[-700,-100,900,0,0,0],[ 100, 700,790,0,0,0]}, ...
    {'P007','asym_right'     ,[ -50,-700,790,0,0,0],[ 850, 150,880,0,0,0]}, ...
    {'P008','asym_right'     ,[ 150, 650,780,0,0,0],[ 900,-120,900,0,0,0]} ...
    };

for i = 1:numel(rows)
    r = rows{i};
    rec = struct();
    rec.pair_id = r{1};
    rec.layout_class = r{2};
    rec.sat1_state = r{3};
    rec.sat2_state = r{4};
    bank(end+1) = rec; %#ok<AGROW>
end
end
