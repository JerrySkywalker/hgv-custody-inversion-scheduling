function bank = stage15_build_staticworld_target_bank_3d()
%STAGE15_BUILD_STATICWORLD_TARGET_BANK_3D
% Representative local target states under static-world semantics.
%
% state = [x_km, y_km, z_km, vx_mps, vy_mps, vz_mps]

bank = struct( ...
    'target_id', {}, ...
    'geometry_class', {}, ...
    'target_state', {});

rows = { ...
    {'T001','front'  ,[ 100,   0, 40, 4500,    0, -20]}, ...
    {'T002','front'  ,[ 200,  80, 42, 4300,  600, -30]}, ...
    {'T003','front'  ,[ 150, -60, 38, 4700, -400, -25]}, ...
    {'T004','oblique',[ 250, 250, 41, 3800, 2200, -40]}, ...
    {'T005','oblique',[ 350, 180, 43, 4000, 1600, -35]}, ...
    {'T006','oblique',[ 280,-220, 39, 3600,-1800, -30]}, ...
    {'T007','grazing',[   0, 350, 40,    0, 4500, -20]}, ...
    {'T008','grazing',[ 120, 420, 42, 1000, 4200, -25]}, ...
    {'T009','grazing',[-120, 380, 37,-1200, 4300, -15]} ...
    };

for i = 1:numel(rows)
    r = rows{i};
    rec = struct();
    rec.target_id = r{1};
    rec.geometry_class = r{2};
    rec.target_state = r{3};
    bank(end+1) = rec; %#ok<AGROW>
end
end
