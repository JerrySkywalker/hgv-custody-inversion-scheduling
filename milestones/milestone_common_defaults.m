function cfg = milestone_common_defaults(base_cfg, overrides)
%MILESTONE_COMMON_DEFAULTS Build shared milestone configuration.

if nargin < 1 || isempty(base_cfg)
    cfg = default_params();
    incoming_milestones = struct();
    incoming_shared_scenarios = struct();
else
    cfg = base_cfg;
    if isfield(base_cfg, 'milestones') && isstruct(base_cfg.milestones)
        incoming_milestones = base_cfg.milestones;
    else
        incoming_milestones = struct();
    end
    if isfield(base_cfg, 'shared_scenarios') && isstruct(base_cfg.shared_scenarios)
        incoming_shared_scenarios = base_cfg.shared_scenarios;
    else
        incoming_shared_scenarios = struct();
    end
end
if nargin < 2 || isempty(overrides)
    overrides = struct();
end

root_dir = cfg.paths.root;

cfg.paths.outputs = fullfile(root_dir, 'outputs');
cfg.paths.output = cfg.paths.outputs;
cfg.paths.milestones = fullfile(cfg.paths.outputs, 'milestones');
cfg.paths.shared_scenarios = fullfile(cfg.paths.outputs, 'shared_scenarios');
cfg.paths.stage13 = fullfile(cfg.paths.outputs, 'stage', 'stage13');
cfg.paths.milestone_outputs = cfg.paths.milestones;
cfg.paths.shared_scenario_outputs = cfg.paths.shared_scenarios;
cfg.paths.stage13_outputs = cfg.paths.stage13;

cfg.milestones = struct();
cfg.milestones.output_root = cfg.paths.milestones;
cfg.milestones.save_figures = true;
cfg.milestones.save_tables = true;
cfg.milestones.save_reports = true;
cfg.milestones.overwrite = true;
cfg.milestones.report_timestamp = datestr(now, 'yyyy-mm-dd HH:MM:SS');
cfg.milestones.baseline_case_mode = 'first_nominal';
cfg.milestones.baseline_case_id = '';
cfg.milestones.baseline_case_label = 'Baseline nominal case';
cfg.milestones.baseline_theta = struct('h_km', 1000, 'i_deg', 60, 'P', 8, 'T', 4, 'F', 0);
cfg.milestones.Tw_baseline = 60;
cfg.milestones.Tw_list = [30, 60, 90, 120];
cfg.milestones.slice_settings = struct( ...
    'h_km', [800, 1000, 1200], ...
    'i_deg', [40, 60, 80], ...
    'P', [4, 6, 8], ...
    'T', [4, 6, 8], ...
    'heading_subset_max', 10, ...
    'target_family', ["nominal", "heading", "critical"], ...
    'sensor_condition', "default");
cfg.milestones.fft_benchmark_sizes = [64, 128, 256, 512];
cfg.milestones.diagnosis_case_mode = 'first_critical';
cfg.milestones.diagnosis_case_id = '';
cfg.milestones.diagnosis_case_label = 'Diagnosis critical case';
cfg.milestones.diagnosis_theta = struct('h_km', 1000, 'i_deg', 60, 'P', 8, 'T', 4, 'F', 0);
cfg.milestones.case_set = "default";

cfg.shared_scenarios = struct();
cfg.shared_scenarios.enable_auto_build = true;
cfg.shared_scenarios.scenario_id = 'baseline_ch4_ch5';
cfg.shared_scenarios.output_root = cfg.paths.shared_scenarios;

cfg.milestones.MA = struct( ...
    'milestone_id', 'MA', ...
    'title', 'truth_baseline', ...
    'reuse_stages', {{'stage12A_truth_baseline_kernel', 'stage12B_truth_case_window_scan'}}, ...
    'case_mode', cfg.milestones.baseline_case_mode, ...
    'case_id', cfg.milestones.baseline_case_id, ...
    'case_label', cfg.milestones.baseline_case_label, ...
    'theta', cfg.milestones.baseline_theta, ...
    'Tw_s', cfg.milestones.Tw_baseline, ...
    'attach_shared_scenarios', true);

cfg.milestones.MB = struct( ...
    'milestone_id', 'MB', ...
    'title', 'inverse_slices', ...
    'reuse_stages', {{'stage12C_inverse_slice_packager', 'stage12D_task_slice_packager', 'stage12E_minimum_design_packager'}}, ...
    'slice_axes', {{'h', 'i', 'P', 'T'}}, ...
    'slice_settings', struct( ...
        'h_km', [800, 900, 1000, 1100, 1200], ...
        'i_deg', [40, 50, 60, 70, 80], ...
        'P', [4, 6, 8, 10], ...
        'T', [4, 6, 8, 10, 12], ...
        'heading_subset_max', cfg.milestones.slice_settings.heading_subset_max, ...
        'target_family', cfg.milestones.slice_settings.target_family, ...
        'sensor_condition', cfg.milestones.slice_settings.sensor_condition), ...
    'task_slice_settings', struct( ...
        'h_km', [800, 900, 1000], ...
        'i_deg', [50, 60, 70], ...
        'P', [6, 8, 10], ...
        'T', [6, 8, 10, 12], ...
        'heading_subset_max', cfg.milestones.slice_settings.heading_subset_max, ...
        'target_family', cfg.milestones.slice_settings.target_family, ...
        'sensor_condition', cfg.milestones.slice_settings.sensor_condition));

cfg.milestones.MC = struct( ...
    'milestone_id', 'MC', ...
    'title', 'window_scale', ...
    'reuse_stages', {{'stage08_boundary_window_sensitivity', 'stage09_validate_window_kernel', 'stage09_validate_single_design'}}, ...
    'Tw_list', cfg.milestones.Tw_list, ...
    'theta', cfg.milestones.baseline_theta);

cfg.milestones.MD = struct( ...
    'milestone_id', 'MD', ...
    'title', 'fft_support', ...
    'reuse_stages', {{'stage10A_truth_structure_diagnostics', 'stage10B_build_bcirc_reference', 'stage10C_fft_spectral_validation', 'stage10D_symmetry_breaking_margin', 'stage10E_screening_acceleration'}}, ...
    'benchmark_sizes', cfg.milestones.fft_benchmark_sizes, ...
    'theta', cfg.milestones.baseline_theta);

cfg.milestones.ME = struct( ...
    'milestone_id', 'ME', ...
    'title', 'worst_window_diagnosis', ...
    'reuse_stages', {{'stage10A_truth_structure_diagnostics', 'stage11_entry'}}, ...
    'case_mode', cfg.milestones.diagnosis_case_mode, ...
    'case_id', cfg.milestones.diagnosis_case_id, ...
    'case_label', cfg.milestones.diagnosis_case_label, ...
    'theta', cfg.milestones.diagnosis_theta);

cfg.milestones = milestone_common_merge_structs(cfg.milestones, incoming_milestones);
cfg.shared_scenarios = milestone_common_merge_structs(cfg.shared_scenarios, incoming_shared_scenarios);
cfg = milestone_common_merge_structs(cfg, overrides);
end

