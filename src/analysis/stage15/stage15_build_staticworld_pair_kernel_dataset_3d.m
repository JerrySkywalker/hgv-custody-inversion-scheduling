function dataset = stage15_build_staticworld_pair_kernel_dataset_3d(box)
%STAGE15_BUILD_STATICWORLD_PAIR_KERNEL_DATASET_3D
% Build 3D staticworld pair-kernel dataset from target bank and pair bank.

targets = stage15_build_staticworld_target_bank_3d();
pairs = stage15_build_staticworld_sensor_pair_bank_3d();

dataset = struct( ...
    'sample_id', {}, ...
    'target_id', {}, ...
    'pair_id', {}, ...
    'geometry_class', {}, ...
    'layout_class', {}, ...
    'box', {}, ...
    'target_state', {}, ...
    'sat1_state', {}, ...
    'sat2_state', {}, ...
    'xi', {}, ...
    'eta', {}, ...
    'kappa2', {}, ...
    'risk_label', {});

idx = 0;
for it = 1:numel(targets)
    for ip = 1:numel(pairs)
        idx = idx + 1;
        sid = sprintf('SW%03d', idx);

        rec = stage15_make_sample_record_3d( ...
            sid, box, ...
            targets(it).target_state, ...
            pairs(ip).sat1_state, ...
            pairs(ip).sat2_state);

        rec.target_id = targets(it).target_id;
        rec.pair_id = pairs(ip).pair_id;
        rec.geometry_class = targets(it).geometry_class;
        rec.layout_class = pairs(ip).layout_class;

        dataset(end+1) = rec; %#ok<AGROW>
    end
end
end
