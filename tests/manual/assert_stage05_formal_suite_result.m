function assert_stage05_formal_suite_result(out)
assert(height(out.legacy_reproduction.grid_table) == 6, ...
    'Unexpected legacy reproduction row count in formal suite smoke.');

assert(height(out.opend_manual_raan.grid_table) == 18, ...
    'Unexpected OpenD manual-RAAN row count in formal suite smoke.');

assert(height(out.closedd_manual_raan.grid_table) == 18, ...
    'Unexpected ClosedD manual-RAAN row count in formal suite smoke.');

assert(all(ismember({'design_id','P','T','Ns'}, out.legacy_reproduction.grid_table.Properties.VariableNames)), ...
    'Legacy reproduction grid_table is missing required columns.');

assert(all(ismember({'design_id','P','T','Ns'}, out.opend_manual_raan.grid_table.Properties.VariableNames)), ...
    'OpenD manual-RAAN grid_table is missing required columns.');

assert(all(ismember({'design_id','P','T','Ns'}, out.closedd_manual_raan.grid_table.Properties.VariableNames)), ...
    'ClosedD manual-RAAN grid_table is missing required columns.');

assert(isfile(char(out.manifest.manifest_mat)), ...
    'Formal suite manifest MAT file was not created.');

assert(isfile(char(out.manifest.manifest_txt)), ...
    'Formal suite manifest TXT file was not created.');

assert(istable(out.summary_table), ...
    'Formal suite summary_table is missing.');

assert(height(out.summary_table) == 13, ...
    'Unexpected number of rows in formal suite summary_table.');

assert(all(ismember( ...
    {'branch','P','T','Ns','min_pass_ratio','max_pass_ratio','mean_pass_ratio'}, ...
    out.summary_table.Properties.VariableNames)), ...
    'Formal suite summary_table is missing required columns.');

assert(isfile(char(out.summary_exports.summary_csv)), ...
    'Formal suite summary CSV was not created.');

assert(isfile(char(out.summary_exports.summary_mat)), ...
    'Formal suite summary MAT was not created.');

assert(isfield(out, 'figure_exports') && isstruct(out.figure_exports), ...
    'Formal suite figure_exports is missing.');

assert(isfile(char(out.figure_exports.manifest_txt)), ...
    'Formal figures manifest TXT was not created.');

required_figure_fields = { ...
    'legacy_best_pass_by_Ns_png', ...
    'legacy_geometry_heatmap_i60_png', ...
    'opend_env_min_DG_png', ...
    'opend_env_min_pass_ratio_png', ...
    'closedd_env_min_joint_margin_png', ...
    'closedd_env_min_pass_ratio_png'};

for i = 1:numel(required_figure_fields)
    f = required_figure_fields{i};
    assert(isfield(out.figure_exports.files, f), ...
        'Formal figures export is missing required field: %s', f);
    assert(isfile(char(out.figure_exports.files.(f))), ...
        'Formal figure file was not created: %s', f);
end

disp('assert_stage05_formal_suite_result passed.');
end
